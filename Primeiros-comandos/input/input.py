#Para coletar valores diretamente do usuário, usamos a função input, que pedirá os valores das variáveis para o usuário. Para utilizá-la, passamos junto a função uma mensagem que corresponde a pergunta que será feita ao usuário:
nome = input("Qual seu nome? ")
idade = input("Qual sua idade? ")
peso = input ("Qual seu peso? ")

print(nome, idade, peso)