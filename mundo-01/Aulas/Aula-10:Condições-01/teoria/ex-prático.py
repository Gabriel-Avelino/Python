# Os códigos condicionais funcionam da seguinte maneira
tempo = int(input('Quantos anos tem seu carro?'))

if tempo <= 3:
    print('Carro novo')
else:
    print('Carro velho')  # Códigos alinhados com identação para dentro serão executados dependendo da condição.
print('--FIM--')  # Códigos alinhados ao canto esquerdo da tela são executados independente da condição.
