# As estruturas condicionais têm o objetivo de criar programas mais complexos que têm a capacidade de realizar caminhos alternativos de acordo com as condições:
# Existem diferentes tipos de estruturas condicionais no Python, a mais simples delas é o if-else:
num = int(input('Digite um número: '))

if num % 2 == 0:
    print('O número {} é par'.format(num))
else:
    print('O número {} é ímpar'.format(num))
# Quando temos somente o if, temos uma estrutura simples, quando temos um if-else, temos uma estrutura composta.
