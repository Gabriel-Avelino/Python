#As variáveis possuem o objetivo de armazenar valores que serão utilizados durante o código. No Python, todas as variáveis são objetos, além disso, o sinal de igual é utilizado para a atribuição de valores às variáveis:
nome = 'Gabriel'
idade = 19
peso = 67.6

print(nome, idade, peso) #Obs: se usarmos sinal de + no lugar, dará erro por causa da diferença de tipos entre as variáveis.