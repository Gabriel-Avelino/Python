for c in range(0, 6):
    print(c)
print('FIM\n\n')  # Contagem de 0 a 5

for c in range(1, 6):
    print(c)
print('FIM\n\n')  # Contagem de 1 a 5

for c in range(1, 7):
    print(c)
print('FIM\n\n')  # Contagem de 1 a 6

for c in range(6, 0, -1):
    print(c)
print('FIM\n\n')  # Contagem de 6 a 0

for c in range(0, 7, 2):
    print(c)
print('FIM\n\n')  # Contagem de 0 a 6 de 2 em 2
