# As funcoes são rotinas de códigos. O Python possui várias funções nativas como 'print', 'int', 'input', 'len', etc.
# Criamos funções para facilitar nosso trabalho. Para criar uma função usamos a seguinte estrutura:

def mostralinha():
    print('-' * 30)


# Para usá-la, fazemos a chamada dessa função:

mostralinha()
print(f'{"Sistema de Alunos":^30}')
mostralinha()
mostralinha()
print(f'{"Cadastro de Funcionários":^30}')
mostralinha()
mostralinha()
print(f'{"Erro do Sistema":^30}')
mostralinha()
